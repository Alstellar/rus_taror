# --- Генераторы системных промптов ---

def get_system_prompt(persona_prompt: str) -> str:
    """
    Собирает системный промпт.
    persona_prompt: Описание роли (из utils/personas.py).
    """
    base_instructions = (
        "Ты работаешь в Telegram-боте. Твоя задача — давать толкования раскладов Таро, гороскопов или снов. "
        "Используй красивое форматирование HTML:\n"
        "- Заголовки выделяй <b>жирным</b>.\n"
        "- Ключевые мысли выделяй <i>курсивом</i>.\n"
        "- Используй эмодзи для живости.\n"
        "- Текст должен быть связным, мистическим, но понятным.\n"
        "- НЕ используй Markdown (звездочки), используй только теги <b> и <i>.\n"
        "Структурируй ответ, чтобы его было удобно читать с телефона."
    )
    return f"{persona_prompt}\n\n{base_instructions}"


# --- Шаблоны запросов (User Prompts) ---

def make_horoscope_prompt(sign: str, date_str: str) -> str:
    return (
        f"Напиши гороскоп для знака <b>{sign}</b> на сегодня ({date_str}). "
        f"Разбей на сферы: Любовь ❤️, Работа 💼, Здоровье 🌿. "
        f"В конце дай краткий 'Совет дня ✨'."
    )


def make_dream_prompt(description: str) -> str:
    return (
        f"Пользователь рассказал свой сон: \"{description}\".\n\n"
        f"Растолкуй этот сон. Выдели ключевые символы и их значение. "
        f"Дай общий психоаналитический или мистический вывод. "
        f"Ответ должен быть успокаивающим или предостерегающим, но полезным."
    )


def make_tarot_prompt(layout_name: str, question: str, cards_info: list) -> str:
    """
    layout_name: Название расклада (например, 'Кельтский крест')
    question: Вопрос пользователя
    cards_info: Список строк вида "Позиция 1: Название карты (Аркан)"
    """
    cards_text = "\n".join(cards_info)

    return (
        f"Расклад: <b>{layout_name}</b>\n"
        f"Вопрос кверента: \"{question}\"\n\n"
        f"Выпавшие карты:\n{cards_text}\n\n"
        f"Дай подробное толкование расклада. Опиши значение каждой карты в её позиции "
        f"применительно к вопросу пользователя. Сделай синтез расклада (общий итог)."
    )